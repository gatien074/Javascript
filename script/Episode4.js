// Définir les deux variables
let variable1 = 10;
let variable2 = 20;

console.log("Avant permutation :");
console.log("variable1 =", variable1);
console.log("variable2 =", variable2);

// Permuter les valeurs
let temp = variable1;
variable1 = variable2;
variable2 = temp;

console.log("Après permutation :");
console.log("variable1 =", variable1);
console.log("variable2 =", variable2);
